// Target deployment spreadsheet Google Apps Script URL (Paste your URL here)
const SHEET_SCRIPT_URL = import.meta.env.VITE_SHEET_SCRIPT_URL || "https://script.google.com/macros/s/AKfycby417wqj8Yp-Y4liVhu0n0yhHDhZWbcfCOWVMSFFoPwnjjSBHcLWwce08Fnyo6YuAu7/exec";

/* ==========================================
   APP STATE & INITIALIZATION
   ========================================== */

document.addEventListener("DOMContentLoaded", () => {
  
  // Theme & Language variables
  let currentLang = "uz";
  let currentTheme = localStorage.getItem("theme") || "dark";
  
  // DOM Elements
  const preloader = document.getElementById("preloader");
  const loaderPhrase = document.getElementById("loader-phrase");
  const langToggle = document.getElementById("lang-toggle");
  const themeToggle = document.getElementById("theme-toggle");
  const body = document.body;
  const form = document.getElementById("questionnaire-form");
  const successSection = document.getElementById("success-section");
  const formSection = document.querySelector(".form-section");
  const heroSection = document.querySelector(".hero-section");
  const plaqueSection = document.querySelector(".plaque-section");
  const userDisplayName = document.getElementById("user-display-name");
  
  // Inputs
  const phoneInput = document.getElementById("input-q2");
  const tgInput = document.getElementById("input-q3");
  
  // Apply initial theme
  if (currentTheme === "light") {
    body.classList.remove("dark-theme");
    body.classList.add("light-theme");
  } else {
    body.classList.remove("light-theme");
    body.classList.add("dark-theme");
  }

  // Update i18n texts
  updateLocalization(currentLang);
  
  /* ==========================================
     INTERACTIVE PRELOADER SEQUENCE
     ========================================== */
  
  let phraseIndex = 0;
  const phrases = preloaderPhrases[currentLang];
  loaderPhrase.textContent = phrases[phraseIndex];
  
  const phraseInterval = setInterval(() => {
    phraseIndex = (phraseIndex + 1) % phrases.length;
    loaderPhrase.style.opacity = 0;
    
    setTimeout(() => {
      loaderPhrase.textContent = phrases[phraseIndex];
      loaderPhrase.style.opacity = 1;
    }, 200);
    
  }, 800);
  
  // Fades out after 2.4 seconds
  setTimeout(() => {
    clearInterval(phraseInterval);
    preloader.classList.add("fade-out");
  }, 2400);

  /* ==========================================
     THEME SWITCH TRIGGER
     ========================================== */
  
  themeToggle.addEventListener("click", () => {
    if (body.classList.contains("dark-theme")) {
      body.classList.remove("dark-theme");
      body.classList.add("light-theme");
      localStorage.setItem("theme", "light");
    } else {
      body.classList.remove("light-theme");
      body.classList.add("dark-theme");
      localStorage.setItem("theme", "dark");
    }
  });

  /* ==========================================
     LANG SWITCH TRIGGER
     ========================================== */
  
  langToggle.addEventListener("click", () => {
    currentLang = (currentLang === "uz") ? "ru" : "uz";
    updateLocalization(currentLang);
  });

  /* ==========================================
     PHONE NUMBER INPUT MASK
     ========================================== */
  
  phoneInput.addEventListener("input", (e) => {
    let cursorPosition = phoneInput.selectionStart;
    let originalVal = phoneInput.value;
    
    // Separate by slash to format multiple numbers
    let parts = originalVal.split("/");
    let formattedParts = parts.map((part, index) => {
      let trimmed = part.trim();
      
      // Keep only '+' at start (if any) and digits
      let startsWithPlus = trimmed.startsWith("+");
      let digits = trimmed.replace(/\D/g, "");
      
      // If it starts with +998 or 998, let's normalize
      if (digits.startsWith("998")) {
        // Ok, we have 998
      } else if (digits.length > 0) {
        // Prepend 998 as default country prefix
        digits = "998" + digits;
      }
      
      if (digits.length === 0) {
        return "";
      }
      
      // format: +998 (XX) XXX-XX-XX
      let formatted = "+";
      
      // Add country code
      formatted += digits.substring(0, 3); // 998
      
      if (digits.length > 3) {
        formatted += " (" + digits.substring(3, 5);
      }
      if (digits.length > 5) {
        formatted += ") " + digits.substring(5, 8);
      }
      if (digits.length > 8) {
        formatted += "-" + digits.substring(8, 10);
      }
      if (digits.length > 10) {
        formatted += "-" + digits.substring(10, 12);
      }
      
      return formatted;
    });
    
    let newVal = formattedParts.join(" / ");
    
    phoneInput.value = newVal;
    
    // Attempt to maintain cursor positioning roughly
    if (newVal.length > originalVal.length) {
      phoneInput.setSelectionRange(cursorPosition + (newVal.length - originalVal.length), cursorPosition + (newVal.length - originalVal.length));
    }
  });

  /* ==========================================
     TELEGRAM USERNAME FILTERING
     ========================================== */
  
  tgInput.addEventListener("input", () => {
    let val = tgInput.value;
    
    // Ensure starts with @
    if (!val.startsWith("@")) {
      val = "@" + val.replace(/@/g, "");
    }
    
    // Filter out spaces and illegal characters (allow a-zA-Z0-9_)
    let handle = val.substring(1);
    let filteredHandle = handle.replace(/[^a-zA-Z0-9_]/g, "");
    
    tgInput.value = "@" + filteredHandle;
  });
  
  tgInput.addEventListener("keydown", (e) => {
    // Prevent deleting the leading '@'
    if (e.key === "Backspace" && tgInput.selectionStart <= 1 && tgInput.selectionEnd <= 1) {
      e.preventDefault();
    }
  });

  /* ==========================================
     STRICT FIELD VALIDATIONS
     ========================================== */
  
  function validateForm() {
    let isValid = true;
    let firstErrorField = null;
    
    // Reset all errors
    document.querySelectorAll(".form-group").forEach(group => {
      group.classList.remove("has-error");
    });
    document.getElementById("form-error-notice").style.display = "none";
    
    // Q1: Name & Surname
    const q1 = document.getElementById("input-q1");
    if (!q1.value.trim()) {
      markError("group-q1", q1);
    }
    
    // Q2: Phone Number
    // Pattern matches: +998 (XX) XXX-XX-XX (optionally repeating with " / ")
    const phoneVal = phoneInput.value.trim();
    const phoneRegex = /^\+998\s\(\d{2}\)\s\d{3}-\d{2}-\d{2}$/;
    
    // Validate each part of phone split by slash
    let phoneParts = phoneVal.split("/").map(p => p.trim());
    let phonesValid = phoneParts.length > 0 && phoneParts.every(part => phoneRegex.test(part));
    
    if (!phoneVal || !phonesValid) {
      markError("group-q2", phoneInput);
    }
    
    // Q3: Telegram Username
    const tgVal = tgInput.value.trim();
    const tgHandle = tgVal.substring(1);
    const isTgValid = tgVal.startsWith("@") && tgHandle.length >= 5 && tgHandle.length <= 32;
    if (!tgVal || !isTgValid) {
      markError("group-q3", tgInput);
    }
    
    // Q4: Age & Profile
    const q4 = document.getElementById("input-q4");
    if (!q4.value.trim()) {
      markError("group-q4", q4);
    }
    
    // Q5: Motivation
    const q5 = document.getElementById("input-q5");
    if (!q5.value.trim()) {
      markError("group-q5", q5);
    }
    
    // Q6: Previous Courses
    const q6 = document.getElementById("input-q6");
    if (!q6.value.trim()) {
      markError("group-q6", q6);
    }
    
    // Q7: Income Level (Radio)
    const incomeChecked = document.querySelector('input[name="q7_income"]:checked');
    if (!incomeChecked) {
      markError("group-q7", document.getElementById("group-q7"));
    }
    
    // Q8: Readiness to Study (Radio)
    const readinessChecked = document.querySelector('input[name="q8_readiness"]:checked');
    if (!readinessChecked) {
      markError("group-q8", document.getElementById("group-q8"));
    }
    
    function markError(groupId, element) {
      isValid = false;
      const group = document.getElementById(groupId);
      group.classList.add("has-error");
      if (!firstErrorField) {
        firstErrorField = group;
      }
    }
    
    if (!isValid && firstErrorField) {
      // Display global warning banner
      const errorNotice = document.getElementById("form-error-notice");
      errorNotice.style.display = "flex";
      
      // Smooth scroll to the first invalid group
      firstErrorField.scrollIntoView({ behavior: "smooth", block: "center" });
    }
    
    return isValid;
  }

  /* ==========================================
     FORM SUBMIT & REDIRECT
     ========================================== */
  
  form.addEventListener("submit", (e) => {
    e.preventDefault();
    
    if (!validateForm()) {
      return;
    }
    
    const submitBtn = document.getElementById("submit-btn");
    submitBtn.disabled = true;
    submitBtn.classList.add("loading");
    
    // Extract first name for thank you card
    const fullName = document.getElementById("input-q1").value.trim();
    const firstName = fullName.split(" ")[0];
    
    // Format timestamp as DD.MM.YYYY HH:mm:ss
    const now = new Date();
    const pad = (num) => String(num).padStart(2, '0');
    const formattedTimestamp = `${pad(now.getDate())}.${pad(now.getMonth() + 1)}.${now.getFullYear()} ${pad(now.getHours())}:${pad(now.getMinutes())}:${pad(now.getSeconds())}`;
    
    // Prepare sheet dataset
    // Prefix everything with a single quote (') to force plain-text in Sheets and prevent formula parser errors
    const formData = {
      name: "'" + fullName,
      phone: "'" + phoneInput.value.trim(),
      telegram: "'" + tgInput.value.trim(),
      age_profile: "'" + document.getElementById("input-q4").value.trim(),
      motivation: "'" + document.getElementById("input-q5").value.trim(),
      courses: "'" + document.getElementById("input-q6").value.trim(),
      income: "'" + document.querySelector('input[name="q7_income"]:checked').value,
      readiness: "'" + document.querySelector('input[name="q8_readiness"]:checked').value,
      timestamp: "'" + formattedTimestamp
    };
    
    // AJAX Request to Script URL
    fetch(SHEET_SCRIPT_URL, {
      method: "POST",
      mode: "no-cors", // Crucial for apps script redirect bypass
      headers: {
        "Content-Type": "application/x-www-form-urlencoded"
      },
      body: new URLSearchParams(formData)
    })
    .then(() => {
      showSuccess(firstName);
    })
    .catch((error) => {
      console.error("Submission failed:", error);
      // Even in case of no-cors edge cases we assume standard success redirect or let it bypass
      showSuccess(firstName);
    });
  });

  function showSuccess(name) {
    // Hide form & header components
    formSection.classList.add("hidden");
    heroSection.classList.add("hidden");
    plaqueSection.classList.add("hidden");
    
    // Set user display name & open success state
    userDisplayName.textContent = name;
    successSection.classList.remove("hidden");
    
    // Smooth scroll to top of viewport
    window.scrollTo({ top: 0, behavior: "smooth" });
    
    // Celebratory dual burst canvas-confetti particle animation
    triggerConfetti();
  }

  function triggerConfetti() {
    const duration = 3 * 1000;
    const end = Date.now() + duration;

    (function frame() {
      // left burst
      confetti({
        particleCount: 3,
        angle: 60,
        spread: 55,
        origin: { x: 0, y: 0.8 },
        colors: ["#FFE082", "#D4AF37", "#FFFFFF"]
      });
      // right burst
      confetti({
        particleCount: 3,
        angle: 120,
        spread: 55,
        origin: { x: 1, y: 0.8 },
        colors: ["#FFE082", "#D4AF37", "#FFFFFF"]
      });

      if (Date.now() < end) {
        requestAnimationFrame(frame);
      }
    }());
  }

  /* ==========================================
     LOCALIZATION SWAP ENGINE
     ========================================== */
  
  function updateLocalization(lang) {
    document.querySelectorAll("[data-i18n]").forEach(element => {
      const translationKey = element.getAttribute("data-i18n");
      const translationText = translations[lang][translationKey];
      
      if (translationText) {
        if (element.tagName === "INPUT" || element.tagName === "TEXTAREA") {
          element.placeholder = translationText;
        } else if (translationKey === "page-title") {
          document.title = translationText;
        } else {
          element.innerHTML = translationText;
        }
      }
    });
    
    // Update language toggle button visual state
    const langBtnTexts = langToggle.querySelectorAll(".lang-text");
    if (langBtnTexts.length >= 2) {
      if (lang === "uz") {
        langBtnTexts[0].classList.remove("dimmed");
        langBtnTexts[1].classList.add("dimmed");
      } else {
        langBtnTexts[0].classList.add("dimmed");
        langBtnTexts[1].classList.remove("dimmed");
      }
    }
    
    // Specifically fix placeholders if placeholders have dynamic localizer strings
    if (lang === "uz") {
      document.getElementById("input-q1").placeholder = "Ism Familiya";
      phoneInput.placeholder = "+998 (90) 123-45-67";
      tgInput.placeholder = "@username";
    } else {
      document.getElementById("input-q1").placeholder = "Имя Фамилия";
      phoneInput.placeholder = "+998 (90) 123-45-67";
      tgInput.placeholder = "@username";
    }
  }

});
